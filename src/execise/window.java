package execise;

public class window {
    private int height = 2;
    private int  width = 3;


    public window(int height, int width) {
        this.height = height;
        this.width = width;

    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }
}

